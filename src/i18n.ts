export type T = typeof translations.uz;

let currentLang: Lang = (localStorage.getItem('bioplusai_lang') as Lang) || 'uz';
const listeners: Array<() => void> = [];

export function getLang(): Lang { return currentLang; }

export function setLang(lang: Lang) {
  currentLang = lang;
  localStorage.setItem('bioplusai_lang', lang);
  listeners.forEach(fn => fn());
}

export function t(key: keyof T): string {
  return ((translations[currentLang] as T)[key] ?? (translations.uz as T)[key]) as string;
}

export function useLang(): [Lang, (l: Lang) => void] {
  const [lang, setLangState] = React.useState<Lang>(currentLang);
  React.useEffect(() => {
    const handler = () => setLangState(getLang());
    listeners.push(handler);
    return () => { const i = listeners.indexOf(handler); if (i > -1) listeners.splice(i, 1); };
  }, []);
  return [lang, (l: Lang) => { setLang(l); setLangState(l); }];
}
